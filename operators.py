#Arithmetic Operators

x = 10
y = 5

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #Modulus-returns the remainder

#Comparison operators-are used to compare values and always return a boolean.

print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y)
print(x != y)

#Assignment Operators

a = 56
print(a)

#Operator Presedence-order in which operations get executed
a += 2
print(a)

#Logical Operators:and, or, not-Used when setting more than one condition at teh same time.

print(100 > 200 and 100 == 200)
print(100 > 200 or 100 == 200)
print(not(100 > 200 or 100 == 200))

